                <p><span class="font-bold">Malang, TandaBaca</span> -- Sepatu emas atau golden shoe menjadi objek yang sedang diperebutkan pencetak gol kawakan Eropa termasuk Kylian Mbappe, Mohamed Salah, dan Robert Lewandowski. Sepatu emas merupakan penghargaan bagi pencetak gol terbanyak di liga domestik di Eropa. Bukan cuma soal membobol gawang lawan, tapi ada perhitungan tersendiri dalam perebutan penghargaan individu ini.</p>
                <br>
                <p>Pencetak gol di lima liga top Eropa berdasarkan penilaian UEFA, mendapat koefisien atau faktor penilai sebanyak 2. Sementara liga berperingkat 6 sampai 22 memiliki koefisien 1,5. Jika liga yang diikuti berada di bawah peringkat 22 maka koefisiennya adalah 1.</p>
                <br>
                <p>Lantaran alasan tersebut Kylian Mbappe, yang kini sudah mencetak 31 gol untuk Real Madrid, berada di atas Viktor Gyokeres yang memproduksi 39 gol untuk Sporting CP.
                Mbappe berada di urutan pertama dengan 62 poin, Gyokeres di peringkat kedua dengan 58,5 poin.
                <br><br>
                Apakah Mbappe akan mendapat penghargaan sepatu emas? Kans besar, tetapi belum pasti.</p>
                <br>
                <p>Masih ada Mohamed Salah yang akan menjalani laga terakhir bersama Liverpool pada Minggu (25/5) malam waktu Indonesia.
                Salah kini memiliki 28 gol atau memiliki 56 poin. Apabila Salah bisa mencetak empat gol saat Liverpool bertemu Crystal Palace, maka pemain asal Mesir itu bakal memiliki poin lebih tinggi ketimbang Mbappe.
                Secara matematis, poin Mbappe juga masih bisa disalip Mateo Retegui, Robert Lewandowski, Alexander Isak, atau Omar Marmoush yang masih akan menjalani satu laga sisa seperti Salah.
                <br><br>
                Hanya saja keempat pemain itu butuh keajaiban karena harus mencetak gol dalam jumlah banyak untuk menggeser Salah dari posisi teratas.
                Retegui sudah mencetak 25 gol untuk Napoli. Begitu pula Lewandowski yang telah melahirkan 25 gol untuk Barcelona. Keduanya butuh tujuh gol agar bisa berada di puncak ranking golden shoe.
                <br><br>
                Sementara Isak yang sudah melesakkan 23 gol di Newcastle United harus mencetak sembilan gol dalam laga terakhir agar bisa menyisihkan Mbappe.
                Tak ubahnya dengan Marmoush yang harus mencetak 10 gol untuk melampaui poin Mbappe lantaran musim ini baru menjaringkan 22 gol untuk Eintracht Frankfurt dan Manchester City.</p>
